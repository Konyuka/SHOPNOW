<script>
import Swal from 'sweetalert2';
import Categeories from '../components/SubNav.vue'
import MainNavigation from '../components/Header.vue'


window.Swal = Swal;
const Toast = Swal.mixin({
  toast: true,
  position: 'top-end',
  showConfirmButton: false,
  timer: 2000,
  timerProgressBar: false,
  didOpen: (toast) => {
    toast.addEventListener("mouseenter", Swal.stopTimer);
    toast.addEventListener("mouseleave", Swal.resumeTimer);
  },
});
window.Toast = Toast;

export default {
    name:'View_Product',
    components: {
        Categeories, 
        MainNavigation
    },
    props: {
        singleProduct: '',
        account:'',
        vendor:''
    },
    computed: {
      cartNumber(){
          return this.$store.state.cartItems.length
      },  
      cartItems(){
          return this.$store.state.cartItems
      }  
    },
    mounted () {
    
    },
    data () {
        return {
            modal:false,
            regModal: false,
            image_src: this.singleProduct.photos,

        }
    },
    methods: {
        addToCart(product){
            var item =  this.cartItems.find(x => x._id == product )
            if(item == undefined){
                this.$store.dispatch('addedToCart', this.singleProduct)
                Toast.fire({
                    icon: 'success',
                    title: 'Added to Cart'
                })
            }else{
                Swal.fire(
                    'Cannot Add Item',
                    'This item is already in your cart',
                    'warning'
                )
                return
            }
            // this.modal = true
        }
    }
  
}
